public class main03 {

	public static void main(String[] args) {

		// switch(variable) ~ case(value)

		int num = 8;
		switch (num) {
		case 1:
			System.out.println("num is 1 ");
			break;
		case 2:
			System.out.println("num is 2 ");
			break;
		case 3:
			System.out.println("num is 3 ");
			break;
		default:
			System.out.println("not match");

		}

	}

}
